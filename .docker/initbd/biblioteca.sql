CREATE TABLE libro(
  titulo VARCHAR(100),
  isbn VARCHAR(10) PRIMARY KEY,
  genero VARCHAR(10),
  autor VARCHAR(100),
  paginas INTEGER
);

DROP TABLE IF EXISTS User;
CREATE TABLE User(
  username VARCHAR(40),
  password VARCHAR(20)
